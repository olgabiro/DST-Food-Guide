# go through all the files in the src/assets/images directory sorted alphabetically
# and rename them to the corresponding name in the NAMES list with a .png extension
# first file in the directory is renamed to the first name in the list, second file to the second name, etc.
# if there are more files than names, the extra files are not renamed
# if there are more names than files, the extra names are ignored
# print out the old and new names of the files as they are renamed

import os
from pathlib import Path

ASSETS_DIR = Path('src/assets/images')

def rename_files():
  if not ASSETS_DIR.exists():
      raise SystemExit(f"Assets directory not found: {ASSETS_DIR}")

  files = sorted(f for f in ASSETS_DIR.iterdir() if f.is_file())
  for i, file in enumerate(files):
      if i >= len(NAMES):
          break
      new_name = NAMES[i] + '.png'
      new_path = ASSETS_DIR / new_name
      os.rename(file, new_path)
      print(f'Renamed {file.name} to {new_name}')


if __name__ == '__main__':
    rename_files()
